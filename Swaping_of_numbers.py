#swaping of two numbers

num1 = int(input("Enter the first number:\n")) 
num2 = int(input("Enter the second number:\n")) 
print("Before swaping:")
print("num1 = ",num1,"&","num2 = ",num2)
swap = num1
num1 = num2
num2 = swap
print("")
print("After swaping:")
print("num1 = ",num1,"&","num2 = ",num2)
